package com.autotrade.entity;

import java.util.Date;

/**
 * @ClassName: BuyingCustomer
 * @description  买车客户实体类
 * @author xiaoyuchao
 * @createDate 2019年1月4日
 */
public class BuyingCustomer {
	/** 主键id */
	private Long id;
	/** 客户姓名 */
	private String name;
	/** 客户电话 */
	private String phone;
	/** 客户家庭地址 */
	private String address;
	/** 品牌 */
	private String brand;
	/** 意向车型 */
	private String intention;
	/** 客户预算 */
	private String budget;
	/** 创建时间 */
	private Date createTime;
	/** 修改时间 */
	private Date updateTime;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getIntention() {
		return intention;
	}
	public void setIntention(String intention) {
		this.intention = intention;
	}
	public String getBudget() {
		return budget;
	}
	public void setBudget(String budget) {
		this.budget = budget;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	@Override
	public String toString() {
		return "BuyingCustomer [id=" + id + ", name=" + name + ", phone=" + phone + ", address=" + address + ", brand="
				+ brand + ", intention=" + intention + ", budget=" + budget + ", createTime=" + createTime
				+ ", updateTime=" + updateTime + "]";
	}
	
}
